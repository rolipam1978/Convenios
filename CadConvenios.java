    private void tfCodEmpresaFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfCodEmpresaFuncionarioActionPerformed
        String sql = "select * from conv_empresas where codempresa= " + tfCodEmpresaFuncionario.getText();
        con_empresa.conecta();
        con_empresa.executeSQL(sql);
        try {
            con_empresa.rs.first();
            tfEmpresaFuncionario.setText(con_empresa.rs.getString("nome"));
            con_empresa.executeSQL("select * from conv_empresas order by codempresa");
            con_empresa.rs.first();
            mostraDados_empresa();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro!");
            e.printStackTrace();
        }
        tfCodEmpresaFuncionario.transferFocus();
    }//GEN-LAST:event_tfCodEmpresaFuncionarioActionPerformed

